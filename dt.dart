void main(List<String> args) {
  // Data Type

  // String
  String firstName = "John";
  print("String: $firstName"); // Output: String: John

  // Integers
  int myNum = 41;
  print("Int = $myNum");

  // Doubles
  double otherNum = 19.95;
  print("Double = $otherNum");

  // Booleans
  bool myBool = false;
  print("Boolean = $myBool");

  // Dynamic
  dynamic fullName = "John Doe";
  print("Dynamic = $fullName");
}